comboboxes = []
selected_flags = []

# コンボボックスの作成（2列構成・幅指定）
def on_select(index):
    selected_flags[index] = True

for i, data in enumerate(combo_data):
    values = [data["label"]] + data["options"]
    row = i % 13 + 13
    col = i // 13
    cb = ttk.Combobox(main_win, values=values, width=20)
    cb.grid(row=row, column=col, padx=10, pady=5)
    cb.set(values[0])
    cb.bind("<<ComboboxSelected>>", lambda e, idx=i: on_select(idx))
    comboboxes.append(cb)
    selected_flags.append(False)

def print_text():
    result = []
    for i, cb in enumerate(comboboxes):
        if selected_flags[i]:
            selected = cb.get()
            if selected in combo_data[i]["mapping"]:
                value = combo_data[i]["mapping"][selected]
                if combo_data[i]["add_comma"]:
                    value = "," + value
                result.append(value)
    label.config(text="".join(result))

def copy_text():
    text_to_copy = label.cget("text")
    main_win.clipboard_clear()
    main_win.clipboard_append(text_to_copy)
    messagebox.showinfo("コピー完了", "テキストがコピーされました。")

button_print = tk.Button(main_win, text="表示", command=print_text)
button_print.grid(row=0, column=1, padx=10)

button_copy = tk.Button(main_win, text="コピー", command=copy_text)
button_copy.grid(row=0, column=0, padx=10)

label = tk.Label(main_win)
label.grid(row=2, column=0, columnspan=2, pady=5)

main_win.mainloop()


